    private void BtnEn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnEn1MouseClicked
        cad=JTA1.getText();
       if(cad.length()>0)
       {   
           cad=cad+" \n";
           Nodo n = new Nodo(cad);
           Nodo m = new Nodo(cad);
           ar.getCol().inserta(n);
           ar.getP1().inserta(m);
           cad=ar.getCol().cadena();
           ar.setNum(ar.getNum() + 1);
           JTAC.setText(cad);
           JTA1.setText(null);
       }
        
    }//GEN-LAST:event_BtnEn1MouseClicked

