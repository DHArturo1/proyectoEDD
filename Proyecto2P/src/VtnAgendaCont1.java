    private void BtnAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnAMouseClicked
         String s = "";
        s = JOptionPane.showInputDialog(grupo + " que desea agregar");
        if (s == null)
        {
            s = "";
        }
        if (s.length() != 0)
        {
            Nodo aux = new Nodo(null, s);
            String[] etqs = new String[2];
            etqs[0] = grupo;
            etqs[1] = s;
            VtnPrincipal.r = Multilistas.inserta(VtnPrincipal.r, aux, 0, etqs);
            actualiza();
            bloquea();
        }
    }//GEN-LAST:event_BtnAMouseClicked

    private void BtnMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnMMouseClicked
        String s = "";
        s = JOptionPane.showInputDialog("Inserta el contacto que  desea mover?");
        if (s == null)
        {
            s = "";
        }
        Nodo aux = r;
        boolean b = false;
        while (aux != null)
        {
            if (aux.getEtq().equals(s))
            {
                b = true;
            }
            aux = aux.getSiguiente();
        }
        if (!b)
        {
            JOptionPane.showMessageDialog(null, "No se encontro a la persona", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
        } else
        {
            if (s.length() != 0)
            {
                String s2 = "";
                s2 = JOptionPane.showInputDialog("Categoria a mover");
                if (s2 == null)
                {
                    s2 = "";
                }
                aux = Multilistas.busca(VtnPrincipal.r, s2);
                if (aux != null)
                {
                    if (s2.length() != 0)
                    {
                        String[] etqsE = new String[2];
                        etqsE[0] = grupo;
                        etqsE[1] = s;
                        String[] etqsI = new String[2];
                        etqsI[0] = s2;
                        etqsI[1] = s;
                        VtnPrincipal.r = Multilistas.mueve(VtnPrincipal.r, 0, etqsE, etqsI);
                        bloquea();
                        actualiza();
                    }                    
                }else{
                    JOptionPane.showMessageDialog(null, "No se encontro la categoria", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
                }
                
            }
        }
    }//GEN-LAST:event_BtnMMouseClicked

