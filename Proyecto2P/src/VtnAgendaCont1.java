    public void actualiza()
    {
        r = Multilistas.busca(VtnPrincipal.r, grupo);
        if(r != null)
        {
            r = r.getAbajo();
        }
        Panel.removeAll();
        if (r != null)
        {
            Nodo aux = r;
            while (aux != null)
            {
                JButton boton = new JButton(aux.getEtq());
                Panel.add(boton);
                boton.addActionListener(new ActionListener()
                {
                    @Override
                    public void actionPerformed(ActionEvent e)
                    {
                        VtnAgendaCont2 chat = new VtnAgendaCont2();
                        chat.grupo= grupo;
                        chat.persona = boton.getText();
                        chat.setVisible(true);
                        chat.actualiza();
                    }
                }
                );
                aux = aux.getSiguiente();
            }
        }
        Panel.updateUI();
    }
